#FIGURE OUT MALE TO FEMALE PERCENTAGES
#the are 8 males and 12 females. There are 20 students in the class. 

males = int(input("enter the number of males in the class:"))
females = int(input("enter the number of females in the class:"))
totalStudents = males + females

malesPercentage =( males/totalStudents)*100
femalesPercentage=(females/totalStudents)*100

print("males percentage:"+str(malesPercentage)+"%")
print("females percentage:"+str(femalesPercentage)+"%")





      
